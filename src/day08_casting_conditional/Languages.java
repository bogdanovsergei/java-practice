package day08_casting_conditional;

public class Languages {
	public static void main(String[] args) {
		int option = 3;
		/*
		 * 1 => English
		 * 2 => Spanish
		 * 3 => French
		 * 4 => Turkish
		 */
		
		if (option == 1)
			System.out.println("Hello!");
		else if (option == 2)
			System.out.println("Hola!");
		else if (option == 3)
			System.out.println("Bonjour!");
		else if (option == 4)
			System.out.println("Merhaba!");
		
	}

}
